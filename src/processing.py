def filter_by_state(bank_operations, state='CANCELED'):
    new_list = []
    for operation in bank_operations:
        if operation['state'] == state:
            new_list.append(operation)
    return new_list

print(filter_by_state([{'id': 41428829, 'state': 'EXECUTED', 'date': '2019-07-03T18:35:29.512364'}, {'id': 939719570, 'state': 'EXECUTED', 'date': '2018-06-30T02:08:58.425572'}, {'id': 594226727, 'state': 'CANCELED', 'date': '2018-09-12T21:27:25.241689'}, {'id': 615064591, 'state': 'CANCELED', 'date': '2018-10-14T08:21:33.419441'}]))


def sort_by_date(bank_operation_sort, reverse=True):
    sort_list = sorted(bank_operation_sort, key=lambda x: x['date'], reverse=reverse)
    return sort_list


